function []=gen_fsup_new();
% This function generate the feature of the superpixel
% which will be shared by subsuperpixels

%%%
% For each image in filename, load
% scratch/data/MedSeg.MediResImgIndexSuperpixelSepi.mat
% generated by gen_Sup_new and call f_sup
% save the results for all images in
% scratch/data/FeatureSuperpixel.mat
%%%

% define general data folder and scratch data folder
global GeneralDataFolder ScratchDataFolder LocalFolder ClusterExecutionDirectory...
    ImgFolder VertYNuPatch HoriXNuPatch a_default b_default Ox_default Oy_default...
    Horizon_default filename;

% prepare data step
tic;
NuPics = size(filename,2); % number of pics
for i=1:NuPics
    i
    % load data
    load([ScratchDataFolder '/data/MedSeg/MediResImgIndexSuperpixelSep' num2str(i) '.mat']);
    % calculate all the features of superpixel
    % don't need to resize all the superpixel images to the same size since
    % all the feature calculated is be normalized to the size the the image
    [FeatureSuperpixel{i}]= f_sup(MediResImgIndexSuperpixelSep); % (hard work 5min) call f_sup to calculate the feature of superpixel
end
save([ScratchDataFolder '/data/FeatureSuperpixel.mat'],'FeatureSuperpixel'); % 
toc;
disp('feature of superpixel ready');
    
